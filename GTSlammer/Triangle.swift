import Foundation
import SpriteKit

class Triangle {
    static func upTriangle() -> CGPath{
        let path = UIBezierPath()
        path.move(to: CGPoint(x: 0.0, y: 0.0))
        path.addLine(to: <#T##CGPoint#>)
    }
}
